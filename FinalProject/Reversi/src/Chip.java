
public enum Chip 
{
	BLACK('B'),WHITE('W'),EMPTY('E');

	private char color;

	private Chip(char color) 
	{
		this.color = color;
	}

	public String toString()
	{
		switch(this){
		case BLACK: return "B";
		case WHITE: return "W";
		case EMPTY: return "E";
		default: throw new IllegalArgumentException(); // had to have an exception to get this to compile. I will catch it later in the code.
		}		
	}
}

